def integer_operations(a, b):
    print("The difference between the two numbers:", a - b)
    print("The product of the two numbers:", a * b)
    print("The average of the two numbers:", (a + b) / 2)
    print("The distance (absolute value of the difference):", abs(a - b))
    print("The maximum (the larger of the two):", max(a, b))
    print("The minimum (the smaller of the two):", min(a, b))

